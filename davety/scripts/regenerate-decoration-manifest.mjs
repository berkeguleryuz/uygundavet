#!/usr/bin/env node
/**
 * Discover-mode manifest generator for decoration templates.
 *
 * Önceki `generate-decoration-templates.mjs` SVG dosyalarını JS kodundan
 * üretiyor + manifest yazıyordu. Sonradan public/ altına manuel eklenen
 * SVG'ler manifest'te görünmediği için süsleme picker'ında çıkmıyordu.
 *
 * Bu script sadece TARAR: `public/assets/templates/` altında ne klasör
 * varsa kategori, içindeki her `*.svg` dosyası ise item olarak manifest'e
 * yazılır. Programmatic generator'ı silmiyoruz — yine kullanılabilir,
 * sonrasında bu script çalıştırılırsa manuel eklenenler de toparlanır.
 *
 * Kullanım:
 *   node scripts/regenerate-decoration-manifest.mjs
 */

import { readdirSync, statSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..", "public", "assets", "templates");
const MANIFEST = resolve(
  __dirname,
  "..",
  "src",
  "components",
  "decorations",
  "templateManifest.ts"
);

// Kategori → görünen Türkçe etiket. Tarama sırasında bilinmeyen klasör
// adıyla karşılaşılırsa fallback olarak klasör adı title-case yapılır.
const LABELS = {
  "wedding-essentials": "Düğün Süslemeleri",
  monograms: "Monogram",
  "save-the-date": "Tarihi Kaydet",
  engagement: "Nişan",
  "henna-night": "Kına Gecesi",
  "botanical-frames": "Botanik Çerçeve",
  "floral-corners": "Çiçekli Köşe",
  "gold-ornaments": "Altın Süsleme",
  dividers: "Ayraç",
  "wax-seals": "Mühür",
  badges: "Rozet",
  "business-luxe": "Minimal Lüks",
  "baby-shower": "Bebek Mevlüdü",
  "kids-party": "Çocuk Partisi",
  "circumcision-classic": "Sünnet Klasik",
};

function titleCase(s) {
  return s
    .split(/[-_]/)
    .map((p) => p.charAt(0).toUpperCase() + p.slice(1))
    .join(" ");
}

const cats = readdirSync(ROOT)
  .filter((name) => {
    const p = join(ROOT, name);
    try {
      return statSync(p).isDirectory();
    } catch {
      return false;
    }
  })
  .sort();

const manifest = cats
  .map((key) => {
    const items = readdirSync(join(ROOT, key))
      .filter((f) => f.toLowerCase().endsWith(".svg"))
      .sort()
      .map((f) => ({
        id: f.replace(/\.svg$/i, ""),
        url: `/assets/templates/${key}/${f}`,
      }));
    return {
      key,
      label: LABELS[key] ?? titleCase(key),
      items,
    };
  })
  .filter((c) => c.items.length > 0);

const total = manifest.reduce((n, c) => n + c.items.length, 0);

writeFileSync(
  MANIFEST,
  `// AUTO-GENERATED by scripts/regenerate-decoration-manifest.mjs — do not edit by hand.
export interface DecorationTemplate {
  id: string;
  url: string;
}

export interface DecorationTemplateCategory {
  key: string;
  label: string;
  items: DecorationTemplate[];
}

export const DECORATION_TEMPLATE_CATEGORIES: DecorationTemplateCategory[] =
  ${JSON.stringify(manifest, null, 2)};

export function findTemplate(id: string): DecorationTemplate | undefined {
  for (const cat of DECORATION_TEMPLATE_CATEGORIES) {
    const found = cat.items.find((i) => i.id === id);
    if (found) return found;
  }
  return undefined;
}
`
);

console.log(
  `\n${total} decoration template indexed across ${manifest.length} categor${
    manifest.length === 1 ? "y" : "ies"
  }.`
);
console.log(`Manifest: ${MANIFEST}`);
